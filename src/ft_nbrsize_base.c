#include "libftprintf.h"

int					ft_nbrsize_base(int n, int base)
{
	int i;

	i = 0;
	if (n < 0)
	{
		if (base == 10)
			i++;
		n *= -1;
	}
	while (n)
	{
		n /= base;
		i++;
	}
	return (i);
}
